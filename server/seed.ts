async function seed() {
  console.log(`Seeding ${QR_DATA.length} QR codes from Excel data...`);

  for (const chunk of chunkArray(QR_DATA, 100)) {
    const values = chunk
      .map(({ qrNumber, redirectPath, destinationUrl }) => {
        const dest = destinationUrl ? `'${destinationUrl.replace(/'/g, "''")}'` : "NULL";
        return `(${qrNumber}, '${redirectPath}', ${dest}, true)`;
      })
      .join(",\n    ");

    await sql.query(`
      INSERT INTO qr_codes (qr_number, redirect_path, destination_url, is_active)
      VALUES ${values}
      ON CONFLICT (qr_number) DO UPDATE SET
        destination_url = EXCLUDED.destination_url,
        is_active = EXCLUDED.is_active,
        redirect_path = EXCLUDED.redirect_path,
        updated_at = NOW()
    `);
    console.log(`  Upserted ${chunk.length} rows (up to QR #${chunk[chunk.length - 1].qrNumber})`);
  }

  console.log("Seed complete.");
}

function chunkArray<T>(arr: T[], size: number): T[][] {
  const chunks: T[][] = [];
  for (let i = 0; i < arr.length; i += size) chunks.push(arr.slice(i, i + size));
  return chunks;
}

seed().catch((err) => {
  console.error("Seed failed:", err);
  process.exit(1);
});
